# 1. Arithmetic Operators
a = 10
b = 3

print(f"a = {a}, b = {b}")
print("Addition (a + b):", a + b)
print("Subtraction (a - b):", a - b)
print("Multiplication (a * b):", a * b)
print("Division (a / b):", a / b)       # Returns float
print("Floor Division (a // b):", a // b) # Returns int (quotient)
print("Modulus (a % b):", a % b)        # Returns remainder
print("Exponentiation (a ** b):", a ** b) # a to the power of b

# 2. Comparison Operators (return True or False)
print("\nComparison:")
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)

# 3. Logical Operators (and, or, not)
x = True
y = False
print("\nLogical:")
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)

